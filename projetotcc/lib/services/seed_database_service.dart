/// Estado alvo para cada página do livro demo.
const List<Map<String, dynamic>> _paginasDemo = [
  {'estado': 'processando', 'numero': 1, 'ehPagina': 1},
  {'estado': 'sucesso', 'numero': 2, 'ehPagina': 1},
  {'estado': 'falha', 'numero': 3, 'ehPagina': 1},
  {'estado': 'rescan', 'numero': 4, 'ehPagina': 1},
  {'estado': 'nao_pagina', 'numero': null, 'ehPagina': 0},
];

/// Serviço para inserir dados de demonstração no banco.
class SeedDatabaseService {
  static const String _demoScanId = 'demo_todos_estados';

  /// Insere um livro demo com uma página em cada estado (processando, sucesso, falha, rescan, nao_pagina).
  /// Só executa em modo debug e se o livro demo ainda não existir.
  static Future<void> seedSeNecessario() async {
    if (!kDebugMode) return;
    if (await ScanDatabase.scanExists(_demoScanId)) return;

    try {
      final dir = await getApplicationDocumentsDirectory();
      final scansDir = Directory('${dir.path}/scans');
      if (!await scansDir.exists()) {
        await scansDir.create(recursive: true);
      }
      final scanDir = Directory('${scansDir.path}/Demo_Todos_Estados_$_demoScanId');
      await scanDir.create(recursive: true);

      final imagens = <Map<String, dynamic>>[];
      for (var i = 0; i < _paginasDemo.length; i++) {
        final p = _paginasDemo[i];
        final ext = 'jpg';
        final path = '${scanDir.path}/page_${(i + 1).toString().padLeft(3, '0')}.$ext';
        await File(path).writeAsBytes(_placeholderJpegBytes);
        imagens.add({
          'scan_id': _demoScanId,
          'caminho': path,
          'ordem': i + 1,
          'formato': ext,
          'estado_target': p['estado'] as String,
          'eh_pagina': p['ehPagina'] as int,
          'numero_pagina': p['numero'] as int?,
          'qualidade_target': p['estado'] == 'sucesso' ? 85 : (p['estado'] == 'falha' ? 20 : null),
        });
      }

      await ScanDatabase.insertScan(
        id: _demoScanId,
        titulo: 'Demo: Todos os Estados',
        autor: 'Sistema',
        resumo: 'Livro de teste com uma página em cada estado (amarelo/verde/vermelho/roxo/cinzento).',
        dataCriacao: DateTime.now().millisecondsSinceEpoch,
        imagens: imagens,
      );
    } catch (_) {
      // Ignorar erros de seed
    }
  }
}
